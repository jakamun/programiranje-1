(*----------------------------------------------------------------------------*]
 Funkcija [insert y xs] vstavi [y] v že urejen seznam [xs] in vrne urejen
 seznam. 
 - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -
 # insert 9 [0; 2];;
 - : int list = [0; 2; 9]
 # insert 1 [4; 5];;
 - : int list = [1; 4; 5]
 # insert 7 [];;
 - : int list = [7]
[*----------------------------------------------------------------------------*)

let insert y list = 
  let rec aux acc = function
  | [] -> List.rev ( y :: acc)
  | x :: [] -> if x <= y then List.rev (y :: x :: acc) else List.rev (x :: y :: acc)
  | x :: xs -> 
  if x > y then (List.rev (y :: acc)) @ [x] @ xs
  else aux (x :: acc) xs
  in
  aux [] list

let rec vstavi y = function
  | [] -> [y]
  | x :: xs -> 
  if x >= y then y :: x :: xs
  else x :: vstavi y xs

(*----------------------------------------------------------------------------*]
 Prazen seznam je že urejen. Funkcija [insert_sort] uredi seznam tako da
 zaporedoma vstavlja vse elemente seznama v prazen seznam.
[*----------------------------------------------------------------------------*)

let insert_sort sez =
  let rec aux acc = function
  | [] -> acc
  | x :: xs -> aux (insert x acc) xs
  in
  aux [] sez

let uredi_z_vstavlanjem xs = List.fold_left (fun acc x -> vstavi x acc) [] xs

